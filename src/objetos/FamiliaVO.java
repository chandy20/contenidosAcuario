    public String getFami_descripcion() {
        return fami_descripcion;
    }

    public void setFami_descripcion(String fami_descripcion) {
        this.fami_descripcion = fami_descripcion;
    }

    public int getOrde_id() {
        return orde_id;
    }

    public void setOrde_id(int orde_id) {
        this.orde_id = orde_id;
    }
    
    
    
    
